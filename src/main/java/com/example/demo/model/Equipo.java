package com.example.demo.model;

public class Equipo {
    private String dominio;
    private String lastEvent;
    private String enFaena;
    private String nombre;
    private String marca;
    private String modelo;
    private double velocidad;
    private String notas;
    private String numChasis;
    private String numFreMot;
    private String diaConsulta;
    private String mesConsulta;
    private String anoConsulta;

    // Getters y Setters

    public String getDominio() {
        return dominio;
    }

    public void setDominio(String dominio) {
        this.dominio = dominio;
    }

    public String getLastEvent() {
        return lastEvent;
    }

    public void setLastEvent(String lastEvent) {
        this.lastEvent = lastEvent;
    }

    public String getEnFaena() {
        return enFaena;
    }

    public void setEnFaena(String enFaena) {
        this.enFaena = enFaena;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public double getVelocidad() {
        return velocidad;
    }

    public void setVelocidad(double velocidad) {
        this.velocidad = velocidad;
    }

    public String getNotas() {
        return notas;
    }

    public void setNotas(String notas) {
        this.notas = notas;
    }

    public String getNumChasis() {
        return numChasis;
    }

    public void setNumChasis(String numChasis) {
        this.numChasis = numChasis;
    }

    public String getNumFreMot() {
        return numFreMot;
    }

    public void setNumFreMot(String numFreMot) {
        this.numFreMot = numFreMot;
    }

    public String getDiaConsulta() {
        return diaConsulta;
    }

    public void setDiaConsulta(String diaConsulta) {
        this.diaConsulta = diaConsulta;
    }

    public String getMesConsulta() {
        return mesConsulta;
    }

    public void setMesConsulta(String mesConsulta) {
        this.mesConsulta = mesConsulta;
    }

    public String getAnoConsulta() {
        return anoConsulta;
    }

    public void setAnoConsulta(String anoConsulta) {
        this.anoConsulta = anoConsulta;
    }
}
